package two;
//Arithmetic operators
//1. + Additive
//2. - Subtraction
//3. * Multiplication
//4. / Division
//5. % Modulo(Remainder)

public class ArithmeticDemonstration {

    public static void main(String[] args) {
        int result = 1 + 2;
        System.out.println("1 + 2 = " + result);
        result = result - 1;
        System.out.println(result);
        result = result * 12;
        System.out.println(result);
        result = result/2;
        System.out.println(result);
        result = result%7;
        System.out.println(result);
        
    }
}
